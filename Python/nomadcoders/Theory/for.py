# for loop
# The for loop is used to iterate over the elements of sequence(such as a string, tuple or list) or other iterable object.

# tuple
days = ("Mon", "Tue", "Wed", "Thur", "Fri")

for x in days:
  if x is "Wed":
    break
  else:
    print(x)

# string
for letter in "Uneune":
  print(letter)
